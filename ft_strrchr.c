#include <string.h>

char    *ft_strrchr(char *s, int c)
{
    int     i;
    int     a;

    a = 0;
    i = 0;
    while (s[i])
    {
        if (s[i] == c)
            a = i;
        i++;
    }
    if (a != 0)
        return (s[a]);
    else
        return (NULL);
}